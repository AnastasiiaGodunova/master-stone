let cardList = document.querySelector('.catalog-list');

let makeElement = function (tagName, className, text) {
  let element = document.createElement(tagName);
  element.classList.add(className);
  if (text) {
    element.textContent = text;
  }
  return element;
};

let renderCards = function (product) {
  let listItem = makeElement('li', 'catalog-item');

  let listItemLink = makeElement('a', 'catalog-item__link');

  let picture = makeElement('img', 'catalog-item__image');
  picture.src = product.imgUrl;
  picture.alt = product.text;
  picture.width = 150;
  picture.height = 150;
  listItemLink.appendChild(picture);

  let title = makeElement('h3', 'catalog-item__title', product.text);
  listItemLink.appendChild(title);
  listItem.appendChild(listItemLink);

  return listItem;
};


